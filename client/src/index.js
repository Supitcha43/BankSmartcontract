const displayUser = async (greeting,contract) => {
    greeting =await contract.methods.sayHello().call();
    $("h2").html(greeting);
}

const updateuser = (greeting,contract,accounts) => {
    let input;
    $("#username").on("change", (e) => {
        input = e.target.value;
    });
    $("#form").on("create",async (e) => {
        e.preventDefualt();
        await contract.methods.updateuser(input).send({from: accounts[0], gas: 40000})
        displayUser(greeting, contract);
    })
}

async function dApp() {
    const web3 = await getweb3();
    const accounts = await web3.eth.getAccounts();
    const contract = await getContract(web3);
    let greeting;
    displayUser(greeting, contract);
    updateuser(greeting,contract,accounts);
}

